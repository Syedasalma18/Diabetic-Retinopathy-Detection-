class Trainer:
    def __init__(self, model, device, criterion, optimizer, scheduler=None):
        self.model = model.to(device)
        self.device = device
        self.criterion = criterion
        self.optimizer = optimizer
        self.scheduler = scheduler

    def train_one_epoch(self, train_loader):
        self.model.train()
        running_loss, correct, total = 0.0, 0, 0

        for inputs, labels in tqdm(train_loader, desc="Training"):
            inputs, labels = inputs.to(self.device), labels.to(self.device)
            self.optimizer.zero_grad()

            outputs = self.model(inputs)
            loss = self.criterion(outputs, labels)
            loss.backward()
            self.optimizer.step()

            running_loss += loss.item() * inputs.size(0)
            _, preds = torch.max(outputs, 1)
            correct += (preds == labels).sum().item()
            total += labels.size(0)

        return running_loss / total, correct / total

    def validate_one_epoch(self, val_loader):
        self.model.eval()
        running_loss, correct, total = 0.0, 0, 0
        all_preds, all_labels = [], []

        with torch.no_grad():
            for inputs, labels in tqdm(val_loader, desc="Validation"):
                inputs, labels = inputs.to(self.device), labels.to(self.device)

                outputs = self.model(inputs)
                loss = self.criterion(outputs, labels)

                running_loss += loss.item() * inputs.size(0)
                _, preds = torch.max(outputs, 1)
                correct += (preds == labels).sum().item()
                total += labels.size(0)

                # Collect predictions and labels for metrics
                all_preds.extend(preds.cpu().numpy())
                all_labels.extend(labels.cpu().numpy())

        return running_loss / total, correct / total, np.array(all_preds), np.array(all_labels)

    def plot_class_distribution_from_loader(self, loader, title):
        """
        Plot class distribution for all data from the DataLoader.

        Args:
            loader (DataLoader): The data loader containing the dataset.
            title (str): Title of the plot.
        """
        # Collect all labels from the DataLoader
        all_labels = []
        for _, labels in loader:
            all_labels.extend(labels.numpy())

        # Count occurrences of each class
        class_counts = np.bincount(all_labels)

        # Plot the distribution
        plt.bar(range(len(class_counts)), class_counts)
        plt.xlabel("Class")
        plt.ylabel("Number of Samples")
        plt.title(title)
        plt.show()


    def plot_loss_and_accuracy(self, epochs_range, train_losses, val_losses, train_accs, val_accs):
        plt.figure(figsize=(10, 5))
        plt.subplot(1, 2, 1)
        plt.plot(epochs_range, train_losses, label="Training Loss")
        plt.plot(epochs_range, val_losses, label="Validation Loss")
        plt.xlabel("Epochs")
        plt.ylabel("Loss")
        plt.title("Loss Trends")
        plt.legend()

        plt.subplot(1, 2, 2)
        plt.plot(epochs_range, train_accs, label="Training Accuracy")
        plt.plot(epochs_range, val_accs, label="Validation Accuracy")
        plt.xlabel("Epochs")
        plt.ylabel("Accuracy")
        plt.title("Accuracy Trends")
        plt.legend()

        plt.tight_layout()
        plt.show()

    def plot_confusion_matrix(self, all_labels, all_preds, class_names):
        cm = confusion_matrix(all_labels, all_preds)
        plt.figure(figsize=(8, 6))
        sns.heatmap(cm, annot=True, fmt="d", cmap="Blues", xticklabels=class_names, yticklabels=class_names)
        plt.xlabel("Predicted Labels")
        plt.ylabel("True Labels")
        plt.title("Confusion Matrix")
        plt.show()

    def train(self, train_loader, val_loader, epochs, class_names, early_stopping=None):
        train_losses, val_losses = [], []
        train_accs, val_accs = [], []

        for epoch in range(epochs):
            print(f"Epoch {epoch+1}/{epochs}")
            train_loss, train_acc = self.train_one_epoch(train_loader)
            val_loss, val_acc, all_preds, all_labels = self.validate_one_epoch(val_loader)

            train_losses.append(train_loss)
            val_losses.append(val_loss)
            train_accs.append(train_acc)
            val_accs.append(val_acc)

            print(f"Train Loss: {train_loss:.4f}, Train Acc: {train_acc:.4f}")
            print(f"Val Loss: {val_loss:.4f}, Val Acc: {val_acc:.4f}")

            if self.scheduler:
                self.scheduler.step(val_loss)

            if early_stopping:
                early_stopping(val_loss)
                if early_stopping.early_stop:
                    print("Early stopping triggered.")
                    break

        print("\nClassification Report:")
        print(classification_report(all_labels, all_preds, target_names=class_names))

        epochs_range = range(1, len(train_losses) + 1)
        self.plot_loss_and_accuracy(epochs_range, train_losses, val_losses, train_accs, val_accs)
        self.plot_confusion_matrix(all_labels, all_preds, class_names)

        return train_losses, val_losses

