class GradCAM:
    def __init__(self, model, target_layer_name):
        """
        Args:
            model (nn.Module): Trained model.
            target_layer_name (str): Target layer path (e.g., "7.1.conv2").
        """
        self.model = model
        self.target_layer_name = target_layer_name
        self.activations = None
        self.gradients = None

    def _get_target_layer(self, model, layer_name):
        """
        Recursively find the target layer by its hierarchical name.
        """
        names = layer_name.split(".")
        layer = model
        for name in names:
            if name.isdigit():
                layer = layer[int(name)]
            else:
                layer = getattr(layer, name)
        return layer

    def generate_cam(self, input_tensor, target_class):
        """
        Generate Grad-CAM heatmap for a specific class.

        Args:
            input_tensor (torch.Tensor): Input image tensor.
            target_class (int): Class index to visualize.

        Returns:
            np.ndarray: Grad-CAM heatmap resized to input dimensions.
        """
        self.model.eval()

        # Find the target layer
        target_layer = self._get_target_layer(self.model, self.target_layer_name)

        # Forward hook to capture activations
        def forward_hook(module, input, output):
            self.activations = output.detach()

        # Backward hook to capture gradients
        def backward_hook(module, grad_input, grad_output):
            self.gradients = grad_output[0].detach()

        # Register hooks
        forward_handle = target_layer.register_forward_hook(forward_hook)
        backward_handle = target_layer.register_full_backward_hook(backward_hook)

        # Perform forward and backward passes
        output = self.model(input_tensor)
        target = output[:, target_class].sum()
        self.model.zero_grad()
        target.backward()

