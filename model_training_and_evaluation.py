class DataPipeline:
    def __init__(self, data_dir, batch_size=32, val_split=0.2, random_state=42):
        self.data_dir = data_dir
        self.batch_size = batch_size
        self.val_split = val_split
        self.random_state = random_state

    def _create_transforms(self):
        train_transform = transforms.Compose([
        transforms.Resize((224, 224)),
        transforms.RandomHorizontalFlip(),
        transforms.RandomRotation(10),
        transforms.ColorJitter(brightness=0.2, contrast=0.2, saturation=0.2, hue=0.1),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
        ])
        val_transform = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
        ])
        return train_transform, val_transform

    def load_data(self):
        train_transform, val_transform = self._create_transforms()
        full_dataset = datasets.ImageFolder(self.data_dir, transform=train_transform)

        # Extract labels for stratified split
        labels = [label for _, label in full_dataset.imgs]

        # Perform stratified split
        train_indices, val_indices = train_test_split(
            np.arange(len(labels)),
            test_size=self.val_split,
            stratify=labels,
            random_state=self.random_state
        )

        # Create subsets
        train_dataset = Subset(full_dataset, train_indices)
        val_dataset = Subset(full_dataset, val_indices)

        # Apply validation transforms to validation dataset
        val_dataset.dataset.transform = val_transform

        # Return all datasets
        return full_dataset, train_dataset, val_dataset

    def create_loaders(self, train_dataset, val_dataset):
        # Oversample the training dataset
        train_labels = [train_dataset.dataset.targets[idx] for idx in train_dataset.indices]
        class_counts = np.bincount(train_labels)
        class_weights = 1.0 / class_counts
        sample_weights = [class_weights[label] for label in train_labels]

        # Use WeightedRandomSampler for oversampling
        train_sampler = WeightedRandomSampler(sample_weights, num_samples=len(train_labels), replacement=True)

        # Oversample the validation dataset
        val_labels = [val_dataset.dataset.targets[idx] for idx in val_dataset.indices]
        val_class_counts = np.bincount(val_labels)
        val_class_weights = 1.0 / val_class_counts
        val_sample_weights = [val_class_weights[label] for label in val_labels]

        val_sampler = WeightedRandomSampler(val_sample_weights, num_samples=len(val_labels), replacement=True)

        # Create DataLoaders with the samplers
        train_loader = DataLoader(train_dataset, batch_size=self.batch_size, sampler=train_sampler)
        val_loader = DataLoader(val_dataset, batch_size=self.batch_size, sampler=val_sampler)

        return train_loader, val_loader

