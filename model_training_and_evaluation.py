class FocalLoss(nn.Module):
    def __init__(self, alpha=None, gamma=2, reduction='mean'):
        """
        Args:
            alpha (list, optional): Class weights (list of floats). Default is None.
            gamma (float, optional): Focusing parameter. Default is 2.
            reduction (str, optional): Specifies reduction to apply to the output: 'none' | 'mean' | 'sum'.
        """
        super(FocalLoss, self).__init__()
        self.alpha = alpha
        self.gamma = gamma
        self.reduction = reduction

    def forward(self, inputs, targets):
        # Compute softmax over the logits
        inputs = F.softmax(inputs, dim=1)

        # Gather the probabilities corresponding to the true class labels
        targets_one_hot = F.one_hot(targets, num_classes=inputs.size(1)).float()
        pt = (inputs * targets_one_hot).sum(dim=1)

        # Compute the focal loss
        log_pt = torch.log(pt)
        focal_loss = -((1 - pt) ** self.gamma) * log_pt

        # Apply class weights (alpha) if provided
        if self.alpha is not None:
            alpha = torch.tensor(self.alpha).to(inputs.device)
            focal_loss *= alpha[targets]

        # Reduction: 'mean', 'sum', or 'none'
        if self.reduction == 'mean':
            return focal_loss.mean()
        elif self.reduction == 'sum':
            return focal_loss.sum()
        else:
            return focal_loss

